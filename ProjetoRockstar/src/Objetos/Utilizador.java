package Objetos;

import java.io.Serializable;
import java.time.LocalDate;

public abstract class Utilizador implements Serializable {
    //////////////////////////////////////////ATRIBUTOS/////////////////////////////////////////////////////////////////
    private String username,pass,nome;
    private LocalDate dataRegisto;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////CONSTRUTORES//////////////////////////////////////////////////////////////

    public Utilizador(String username, String pass, String nome) {
        this.username = username;
        this.pass = pass;
        this.nome = nome;
        dataRegisto.now();
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////GETTERS///////////////////////////////////////////////////////////////////
    public String getUsername() {
        return username;
    }
    public String getPass() {
        return pass;
    }
    public String getNome() {
        return nome;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////MÉTODOS DIVERSOS//////////////////////////////////////////////////////////

    /**
     * Este método recebe uma String password e verfica se a mesma corresponde À mesma do utilizador, devolvendo true em
     * caso afirmativo e false em caso negativo.
     */
    public boolean verificaLoginPass(String pass) {
        if (this.pass.equals(pass)) {
            return true;
        } else
            return false;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}
