package Objetos;

import java.io.Serializable;
import java.time.LocalDate;

public class Preco implements Serializable {
    //////////////////////////////////////////ATRIBUTOS/////////////////////////////////////////////////////////////////
    private double valor;
    private LocalDate dataPreco;
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////CONSTRUTORES//////////////////////////////////////////////////////////////
    public Preco(double valor) {
        this.valor = valor;
        this.dataPreco = LocalDate.now();
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////GETTERS/////////////////////////////////////////////////////////////////
    public double getValor() {
        return valor;
    }

    public LocalDate getDataPreco() {
        return dataPreco;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}
